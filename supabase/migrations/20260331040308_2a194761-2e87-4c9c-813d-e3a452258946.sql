
-- =============================================
-- PART 1: EMPRESA + CORE TABLES (tables that others reference)
-- =============================================

-- EMPRESA
CREATE TABLE IF NOT EXISTS public.empresa (
    empresa_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    razao_social text DEFAULT 'Empresa Padrão' NOT NULL,
    nome_fantasia text DEFAULT '' NOT NULL,
    cnpj character varying(18) DEFAULT '' NOT NULL,
    excluido boolean DEFAULT false,
    dt_cadastro timestamp with time zone DEFAULT now(),
    dt_alteracao timestamp with time zone DEFAULT now()
);

-- AUDITORIA
CREATE TABLE IF NOT EXISTS public.auditoria (
    id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    xtabela text NOT NULL,
    xregistro_id text NOT NULL,
    xacao text NOT NULL,
    xdados_anteriores jsonb,
    xdados_novos jsonb,
    xusuario_id uuid,
    xdt_auditoria timestamp with time zone DEFAULT now(),
    xip text DEFAULT '' NOT NULL,
    xobs text DEFAULT '' NOT NULL
);

-- BANCO
CREATE TABLE IF NOT EXISTS public.banco (
    banco_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome text NOT NULL,
    razao_social text DEFAULT '' NOT NULL,
    empresa_id integer DEFAULT 1 NOT NULL,
    excluido boolean DEFAULT false,
    dt_cadastro timestamp with time zone DEFAULT now(),
    dt_alteracao timestamp with time zone
);

-- CIDADE
CREATE TABLE IF NOT EXISTS public.cidade (
    cidade_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descricao text NOT NULL,
    uf character varying(2) DEFAULT 'PR',
    empresa_id integer DEFAULT 1 NOT NULL,
    excluido boolean DEFAULT false,
    dt_cadastro timestamp with time zone DEFAULT now(),
    dt_alteracao timestamp with time zone
);

-- GRUPO_CADASTRO
CREATE TABLE IF NOT EXISTS public.grupo_cadastro (
    grupo_cadastro_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    empresa_id bigint DEFAULT 1 NOT NULL,
    nome character varying NOT NULL,
    dt_cadastro timestamp with time zone DEFAULT now(),
    dt_alteracao timestamp with time zone DEFAULT now(),
    excluido boolean DEFAULT false
);

-- PRODUTO_GRUPO
CREATE TABLE IF NOT EXISTS public.produto_grupo (
    grupo_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    empresa_id bigint DEFAULT 1 NOT NULL,
    nome character varying(50) NOT NULL,
    ultima_sequencia integer DEFAULT 0,
    excluido boolean DEFAULT false,
    dt_cadastro timestamp with time zone DEFAULT now(),
    dt_alteracao timestamp with time zone DEFAULT now()
);

-- CADASTRO
